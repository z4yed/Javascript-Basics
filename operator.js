// Arithmetic Operators
// +, -, *, /, %

console.log(5+5)
console.log(5-5)
console.log(5*5)
console.log(5/5)
console.log(10%3) // modulus operator ; returns remainder

// Increment, Decrement
// ++, --

let x = 1000

console.log(x++) // Post Increment; prints 1000 & increment 1
console.log(x--) //Post Decrement; prints 1001 and decrement 1


x += 40 // x = x + 40

console.log(40)


// Comparison Operators
// <, >, ==, <=, >=, !=, ===
// Returns either True or False


let p = 1000
let q = '1000'

console.log(p == q) // Return True, type doesn't matter
console.log(p === q) // Returns False, type matters.
console.log(p !== q)  // Returns True

